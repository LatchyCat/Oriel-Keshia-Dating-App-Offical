//? Function to create a random array of emojis for a specific season
function randomEmojiArray() {
  const seasonArrays = [summer, winter, fall, spring, halloween, christmas, valentines, easter, independenceDay, thanksgiving, beach, space, food, animals, sports, music, nature, technology, transportation, fantasy];
  const randomSeasonIndex = Math.floor(Math.random() * seasonArrays.length);
  return seasonArrays[randomSeasonIndex];
}

//? Function to create a cool design with emojis
function coolDesign() {
  let design = "";
  const emojisArray = randomEmojiArray(); //* Get a random array of emojis for a specific theme/occasion

  const terminalWidth = process.stdout.columns; //! Get the width of the terminal

  //! Calculate the number of columns and rows based on the terminal width and the number of emojis
  const numCols = Math.ceil(Math.sqrt(emojisArray.length * (terminalWidth / 2))); //* (terminalWidth / 2) to approximate the aspect ratio of emojis
  const numRows = Math.ceil(emojisArray.length / numCols);

  //* Loop through each row
  for (let i = 0; i < numRows; i++) {
    //* Loop through each column
    for (let j = 0; j < numCols; j++) {
      const index = i * numCols + j;
      if (index < emojisArray.length) {
        design += emojisArray[index] + " ";
      }
    }
    design += "\n";
  }

  //* Print the design
  console.log(design);
  return emojisArray;
}

